#Ввести два списка с клавиатуры одинаковой длины (сделать проверку на длину с помощью условия if).
#Преобразовать их в словарь и выполнить следующие операции: добавить элемент в словарь и обновить все текущие
#элементы, вывести длину словаря, вывести все ключи и значения словаря в виде списка.

list1 = input("Введите первый список через пробел: ").split()
list2 = input("Введите второй список через пробел: ").split()

# Проверка на одинаковую длину списков
if len(list1) != len(list2):
    print("Списки должны быть одинаковой длины.")
else:
    # Преобразование списков в словарь
    my_dict = dict(zip(list1, list2))
    print("Исходный словарь:")
    print(my_dict)

    key_to_add = input("Введите ключ для добавления в словарь: ")
    value_to_add = input("Введите значение для добавления в словарь: ")
    my_dict[key_to_add] = value_to_add

    # Обновление всех текущих элементов
    for key in my_dict:
        new_value = input(f"Введите новое значение для ключа '{key}': ")
        my_dict[key] = new_value

    # Вывод длины словаря
    print(f"Длина словаря: {len(my_dict)}")

    # Вывод ключей и значений словаря в виде списка
    keys_list = list(my_dict.keys())
    values_list = list(my_dict.values())
    print("Ключи словаря:")
    print(keys_list)
    print("Значения словаря:")
    print(values_list)
